# Ejemplos de operadores relacionales

a = 10
b = 5

# igual a 

resultado_igual = (a == b)
print(f"{a} es igual a {b}? {resultado_igual}")

# no igual a 
resultado_no_igual = (a != b)
print(f"{a} es diferente de {b}? : {resultado_no_igual}")

# Mayor qué:
resultado_mayor_que = (a > b)
print(f"{a} es mayor que {b}? : {resultado_mayor_que} ")

#menor qué

resultado_menor_que = (a < b)
print(f"{a} es menor que {b}? : {resultado_menor_que} ")

# Mayor o igual qué

resultado_mayor_o_igual_que = (a >= b)
print(f"{a} es mayor o igual que {b}? : {resultado_mayor_o_igual_que} ")


#Menor o igual qué

resultado_menor_o_igual_que = (a <= b)
print(f"{a} es menor o igual que {b}? : {resultado_menor_o_igual_que} ")